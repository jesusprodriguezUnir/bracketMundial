export function generateGroupMatches() {
  const groups = 'ABCDEFGHIJKL'.split('');
  const matches: Array<{
    id: string;
    matchId: string;
    group: string;
    teamA: string;
    teamB: string;
    matchDay: number;
    date: string;
    venue: string;
    city: string;
    scoreA: number | null;
    scoreB: number | null;
  }> = [];
  let mId = 1;

  for (const g of groups) {
    const teams = TEAMS_2026.filter(t => t.group === g).map(t => t.id);
    const fixtures = [
      [0, 1], [2, 3],
      [0, 2], [1, 3],
      [0, 3], [1, 2],
    ];
    fixtures.forEach(([i, j], dayIdx) => {
      const venueIdx = (mId - 1) % VENUES.length;
      matches.push({
        id: `G${g}-${mId}`,
        matchId: `G${g}-${mId++}`,
        group: g,
        teamA: teams[i],
        teamB: teams[j],
        matchDay: Math.floor(dayIdx / 2) + 1,
        date: MATCH_DAYS[Math.floor(dayIdx / 2)].date,
        venue: VENUES[venueIdx],
        city: CITIES[venueIdx],
        scoreA: null,
        scoreB: null,
      });
    });
  }
  return matches;
}

